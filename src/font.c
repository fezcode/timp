static const uint8_t* glyph_for(unsigned char c) {
    if (c >= 'a' && c <= 'z') c = (unsigned char)(c - 'a' + 'A');
    if (c < 32 || c >= 128) return glyphs[0];
    return glyphs[c - 32];
}

void font_draw(SDL_Renderer* ren, int x, int y, int scale,
               SDL_Color color, const char* text) {
    if (scale < 1) scale = 1;
    SDL_SetRenderDrawColor(ren, color.r, color.g, color.b, color.a);
    int cx = x;
    for (const char* p = text; *p; p++) {
        const uint8_t* g = glyph_for((unsigned char)*p);
        for (int row = 0; row < FONT_H; row++) {
            uint8_t bits = g[row];
            for (int col = 0; col < FONT_W; col++) {
                if (bits & (1 << (FONT_W - 1 - col))) {
                    SDL_Rect r = { cx + col * scale, y + row * scale, scale, scale };
                    SDL_RenderFillRect(ren, &r);
                }
            }
        }
        cx += (FONT_W + 1) * scale;
    }
}

int font_text_width(int scale, const char* text) {
    if (scale < 1) scale = 1;
    int n = (int)strlen(text);
    if (n <= 0) return 0;
    return n * (FONT_W + 1) * scale - scale;
}
